public class PerfectDay {
  public static void main(String[] args) {
    System.out.println("Here's my perfect day:");
    morning();
    System.out.println();
    afternoon();
    System.out.println();
    evening();

  }// end main method

  public static void morning() {
    System.out.print("In the morning, ");
    System.out.println("I would wake up around 9:30.");
    System.out.println("I would eat a big breakfast and watch TV.");
    System.out.println("I would then go help out my Dad and Mom.");
  }// end morning method

  public static void afternoon() {
    System.out.print("In the afternoon, ");
    System.out.println("I would go play some basketball.");
    System.out.println("I would then ride my bike for a few miles.");
    System.out.println("I would then come home and eat lunch.");
  }// end afternoon method

  public static void evening() {
    System.out.print("In the evening, ");
    System.out.println("I would relax at home with my family.");
    System.out.println("I would practice on my guitar.");
    System.out.println("I would then go to sleep around 11:00.");
  }// end evening method

}// end class
